	// Define the MethodDemo class
public class MethodDemo {

    // Main method where the program starts execution
    public static void main(String[] args) {
        // Call the greet method
        greet();

        // Call the printMessage method
        printMessage();
    }

    // A method that prints a greeting message
    public static void greet() {
        System.out.println("Hello from the greet method!");
    }

    // A method that prints a specific message
    public static void printMessage() {
        System.out.println("This is my message!");
    }
}
	